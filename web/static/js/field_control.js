// Функция для определения високосного года
function isLeapYear(year) {
    return (year % 4 === 0 && year % 100 !== 0) || (year % 400 === 0);
}

// Функция для обновления дней в зависимости от месяца и года
function updateDays() {
    const selectedYear = parseInt(yearsSelect.value);
    const selectedMonth = parseInt(monthsSelect.value);

    // Определяем количество дней в месяце
    let daysInMonth;
    if (selectedMonth === 2) { // февраль
        daysInMonth = isLeapYear(selectedYear) ? 29 : 28;
    } else if ([4, 6, 9, 11].includes(selectedMonth)) { // апрель, июнь, сентябрь, ноябрь
        daysInMonth = 30;
    } else { // остальные месяцы
        daysInMonth = 31;
    }

    // Сохраняем текущий выбранный день
    const currentDay = daysSelect.value;

    // Очищаем список дней
    daysSelect.innerHTML = '';
    
    const placeholderOption = document.createElement("option");
    placeholderOption.value = "";
    placeholderOption.textContent = "День";
    placeholderOption.disabled = true; // чтобы его нельзя было выбрать после выбора другого
    placeholderOption.selected = true; // чтобы он был выбран по умолчанию
    daysSelect.appendChild(placeholderOption);

    // Заполняем список днями по количеству дней в месяце
    for (let i = 1; i <= daysInMonth; i++) {
        const option = document.createElement("option");
        const day_view = i < 10 ? `0${i}` : `${i}`;
        option.value = day_view;
        option.textContent = i;
        daysSelect.appendChild(option);
    }

    // Если ранее выбранный день больше нового максимума, выбираем последний допустимый день
    if (currentDay && parseInt(currentDay) <= daysInMonth) {
        daysSelect.value = currentDay;
    }
    else if (currentDay && parseInt(currentDay) > daysInMonth){
        // Иначе выбираем последний день месяца по умолчанию
        daysSelect.value = daysInMonth < 10 ? `0${daysInMonth}` : `${daysInMonth}`;
    } 
}

